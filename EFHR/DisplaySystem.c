void SystemMessage(short MessageType) {

	CurPos(12, 8);

	for (short i = 0; i < 33; ++i) putchar(' ');

	CurPos(12, 8);

	switch (MessageType) {

	case BuildingPhase:

		printf("건물 건설을 진행해주세요.");
		break;

	case EnterProductionPhase:

		for (int i = 0; i < 4; ++i) {

			CurPos(12, 8);

			for (short i = 0; i < 33; ++i) putchar(' ');

			CurPos(12, 8);

			StringColor(Cyan);

			if (i % 2) printf("생산 단계에 진입합니다.");
			else printf("Please Wait...");

			Sleep(500);
		}

		StringColor(White);
		break;

	case ProductionPhase:

		printf("자원 생산을 진행해주세요.");
		break;

	case AlreadyOccupied:

		StringColor(Red);
		printf("이미 건물을 지어진 공간입니다.");
		StringColor(White);
		break;

	case NotingLeft:

		StringColor(Red);
		printf("남은 건물이 없습니다.");
		StringColor(White);
		break;

	case Confirm:

		StringColor(Green);
		printf("건설하시려면 한 번 더 눌러주세요.");
		StringColor(White);
		break;

	case Canceled:

		StringColor(Red);
		printf("취소되었습니다.");
		StringColor(White);
		break;

	default:

		StringColor(Red);
		printf("Error, Someting Went Wrong");
		StringColor(White);
		break;
	}

	CurPos(0, 23);
}

